                        <h3 id="why-are-the-forex-markets-trading-times-important" class="pt-4" style="font-size: 20px;"><b>Why are the forex market's trading times important?</b></h3>
                        <p>
                            The FX market is the largest financial market in the world. Forex trading (exchange of currencies) does not occur in a centralised location but rather between participants via phone, online mediums and electronic communication networks (ECNs). This is a global operation taking place round the clock. <br><br>

                            Currencies are required worldwide for international trade, by central banks, and worldwide corporations. In 1971, the gold standard was finally abandoned by the US, and central banks have been heavily dependent on foreign exchange markets. Since then, foreign currencies have been "floated", that is, trade relative to each other, rather than bound to the value of gold.<br><br>

                            The forex market's capacity to operate 24 hours a day is partly due to numerous worldwide time zones and the fact that trades are conducted across a global network. There is one physical exchange that closes at a specific time of day. For example, when you hear that the US dollar closed at a given price, it simply means that it was the price at market close in New York. Unlike securities, currencies are traded globally well after New York closes.
                        </p>
