                        <h4 class="pt-3"><b>Factors that affect day trading</b></h4>
                        <p>
                            The first step in becoming a day trader is deciding which products you want to trade. Day trading using derivatives is popular. Spread betting and CFD trading are the most popular options among retail traders. <br><br>

                            There is no requirement to possess the underlying asset while trading using these products. Instead, day traders typically use derivative products which are traded on margin. This means they use leverage, which magnifies both the potential profits and losses. But you can open and close trades quickly and speculate on whether a market's price is rising or declining.
                        </p>

                        <h4 class="pt-3"><b>Creating a trading plan</b></h4>
                        <p>
                            Every trader should plan exactly what they hope to achieve and be realistic about the goals they set for themselves. You may be disappointed if you anticipate making a lot of money immediately because day trading can have a steep learning curve. <br><br>

                            It is also critical to determine how you will develop a system for entering and exiting trades and whether this will be based on fundamental or technical research. If you opt for fundamental research, your day trades will almost certainly concentrate on macroeconomic data announcements, corporate reports, and breaking news. If you want to employ technical analysis, you will likely concentrate on chart patterns, historical data, and technical indicators.
                        </p>

                        <h4 class="pt-3"><b>Risk management</b></h4>
                        <p>
                            Developing a risk management plan is a critical stage in trading preparation. Traders can minimise possible losses to prevent the worst-case situation with the help of stop losses. Stops and limits are critical risk management tools in every trader's armoury. <br><br>

                            It's commonly said that an experienced trader closes a losing trade quickly but leaves profitable trades to run, and this is true for day trading as much as any other style. A trader does not always have to be correct, but they must immediately recognise when things are going wrong and take action to minimise losses. <br><br>

                            The risk-to-reward ratio is not a static concept. Different market conditions and strategies have different risk-to-reward ratios. However, the goal remains the same, to win more and lose less. <br><br>

                            For example, many day traders are comfortable with a win-to-lose probability of under 40%, but aim for a risk-to-reward ratio of at least 1:2. So for a set of 10 trades ($10 risk per trade), if they win only four trades, they still make $20 ($80 total profit-$60 six losses). It’s to be noted that in this example, the figures are only for illustration and may vary for everyone.<br><br>

                            As a rule of thumb, it's said that you should only risk 1-2 % of your total account on a single trade. 
                        </p>

                        <h4 class="pt-3"><b>Monitoring trades</b></h4>
                        <p>
                            While day trading, you can go long and short on the same day. If you believe a market will rise, you will buy the asset, but if you believe a market will fall, you will choose to sell it.<br><br>

                            It's critical to stay updated on upcoming market events or breaking news that might affect the prices of the markets you're interested in. You can look at an economic calendar at the start of each trading day so that you can trade potential market movements.<br><br>

                            One of the most critical practices at this time is to keep a  journal to record all trades throughout the day and a record of wins and losses. 
                        </p>

                        <h4 class="pt-3"><b>Discipline</b></h4>
                        <p>
                            Many day traders lose money because they fail to generate trade ideas that satisfy their own trading strategy or plan. With trading, as in any other field, success is impossible without discipline.<br><br>

                            To profit, day traders rely mainly on market volatility. A day trader may find a stock interesting if it moves a lot during the day. That might happen for various reasons, including an earnings report, investor mood, or even broad economic or corporate news.<br><br>

                            Day traders also appreciate highly liquid equities since that allows them to adjust their position without influencing the price of the stock.  This liquidity will mean that they can open and exit their positions in one go without the price moving against them. Ultimately it all comes down to the strategy and execution of that strategy with discipline. <br><br>

                            Regardless of the day trader's technique, they're usually looking to trade an asset that moves a lot.
                        </p>

                        <h4 class="pt-3"><b>Realistic profits</b></h4>
                        <p>
                            We see people opening trades on small timeframes intraday. But sometime after entering, and once the price goes in their favour, they will jump to a higher timeframe to justify keeping the trade open for longer. This runs the risk of the price reversing, so their unrealised profits evaporate and the market goes on to hit their stop loss. 
                        </p>

                        <h4 id="why-is-day-trading-controversial" class="pt-3"><b>Why is day trading controversial?</b></h4>
                        <p>
                            The profitability of day trading is a frequently disputed issue on Wall Street. Online day-trading scams have attracted beginners by promising huge profits quickly. <br><br>

                            Some people engage in day trading without adequate understanding. However, other day traders succeed despite – or perhaps because of – the dangers.<br><br>

                            Profiting via day trading is possible. But success isn’t guaranteed due to the risk and expertise required. And don't underestimate the importance of chance and timing. 
                        </p>

                        <h4 id="why-is-day-trading-more-difficult-than-passive-investing" class="pt-3"><b>Why is day trading more difficult than passive investing?</b></h4>
                        <p>
                            Day traders are prone to take more risks than swing or position traders. In day trading, institutional traders and banks compete with retail day traders. Professionals are aware of the downsides and techniques. They have high-tech trading equipment, data subscriptions, and personal relationships. They're well-equipped to succeed. <br><br>

                            Day trading can be tough for retail traders due to psychological biases. Day traders often fall prey to FOMO (the fear of missing out) and open a position too early without a proper signal. Or, they habitually close winners too soon and hold losers too long. 
                        </p>

                        <h4 id="final-thoughts" class="pt-3"><b>Final thoughts</b></h4>
                        <p>
                            Traders use day trading to reduce the danger of slippage or to save money on overnight swaps. Day trading requires a significant amount of time and commitment. Hence it is not typically employed by part-time traders. Stocks, indices, and FX are popular day trading markets.<br><br>

                            Before beginning to day trade, it is critical to analyse a market's liquidity, volatility, and trading volume. You can utilise a variety of day trading tactics, such as trend trading, scalping, swing trading, mean reversion, and money flows. 
                        </p>
